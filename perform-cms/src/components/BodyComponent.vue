<script setup lang="ts">
import { ref, onMounted } from 'vue'
import { watch } from 'vue'

const props = defineProps(['height', 'muscleSelected', 'setMuscleSelected', 'viewOnly'])
const muscleClicked = (event) => {
  // Récupérer l'élément SVG cliqué
  const target = event.target

  // Vérifier si l'élément cliqué est un muscle
  if (target.classList.contains('muscle')) {
    // Récupérer les données du muscle cliqué
    const muscleKey = target.getAttribute('data-muscle')

    const muscles = document.querySelectorAll('[data-muscle="' + muscleKey + '"]')
    Array.from(muscles).forEach((muscle) => {
      const fill = muscle.getAttribute('fill')
      if (muscle.getAttribute('selected') && fill) {
        muscle.style.fill = '#E9E9E9'
        muscle.removeAttribute('selected')
        props.setMuscleSelected(muscleKey, 'remove')
      } else if (fill) {
        props.setMuscleSelected(muscleKey, 'add')
        muscle.style.fill = 'red'
        muscle.setAttribute('selected', 'true')
      }
    })
    // Déclencher un événement ou effectuer une action en fonction du muscle cliqué
    //console.log('Muscle cliqué:', muscle)
  }
}

watch(
  () => props.muscleSelected,
  (newValue, oldValue) => {
    console.log("oui " , props.muscleSelected)
    if(props.viewOnly === "edit" || props.viewOnly === "add") {
      props.muscleSelected.map((muscle) => {
        console.log(muscle)
